"""
–í –¥–∞–Ω–Ω–æ–º –º–æ–¥—É–ª–µ –±—É–¥—É—Ç —Ö—Ä–∞–Ω–∏—Ç—å—Å—è –¥–≤–µ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã. –û–¥–Ω–∞ —Å –∫–Ω–æ–ø–∫–∞–º–∏ "–î–∞–≤–∞–π!" –∏ "–ù–µ —Ö–æ—á—É!",
 –∞ –≤—Ç–æ—Ä–∞—è —Å –∏–≥—Ä–æ–≤—ã–º–∏ –∫–Ω–æ–ø–∫–∞–º–∏ "–ö–∞–º–µ–Ω—å üóø", "–ù–æ–∂–Ω–∏—Ü—ã ‚úÇ" –∏ "–ë—É–º–∞–≥–∞ üìú".
 –ü—Ä–∏—á–µ–º, –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –µ—â–µ —Ä–∞–∑ –ª—É—á—à–µ –ø–æ–Ω—è—Ç—å —Ä–∞–∑–Ω–∏—Ü—É –º–µ–∂–¥—É —Å–æ–∑–¥–∞–Ω–∏–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä
 —Å –ø–æ–º–æ—â—å—é –±–∏–ª–¥–µ—Ä–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä –∏ –±–µ–∑ –Ω–µ–≥–æ, —Ä–µ–∞–ª–∏–∑—É–µ–º –æ–±–∞ –≤–∞—Ä–∏–∞–Ω—Ç–∞.
 –ü–µ—Ä–≤–∞—è –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –±—É–¥–µ—Ç —Å –±–∏–ª–¥–µ—Ä–æ–º, –∞ –≤—Ç–æ—Ä–∞—è –±–µ–∑.
"""

from aiogram.types import ReplyKeyboardMarkup, KeyboardButton
from aiogram.utils.keyboard import ReplyKeyboardBuilder

from lexicons.lexicon_ru import LEXICON_RU

#—Å–æ–∑–¥–∞–µ–º –∫–Ω–æ–ø–∫–∏ —Å –æ—Ç–≤–µ—Ç–∞–º–∏ —Å–æ–≥–ª–∞—Å–∏—è –∏ –æ—Ç–∫–∞–∑–∞
button_yes = KeyboardButton(text=LEXICON_RU["yes_button"])
button_no = KeyboardButton(text=LEXICON_RU["no_button"])

#–∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –±–∏–ª–¥–µ—Ä (—É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –±–∏–ª–¥–µ—Ä) –¥–ª—è –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã —Å –∫–Ω–æ–ø–∫–∞–º–∏ "–î–∞–≤–∞–π" –∏ "–ù–µ —Ö–æ—á—É"
yes_no_kb_builder = ReplyKeyboardBuilder()

#–¥–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –≤ –±–∏–ª–¥–µ—Ä —Å –∞—Ä–≥—É–º–µ–Ω—Ç–æ–º width = 2
yes_no_kb_builder.row(button_yes,button_no, width=2)

#—Å–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏—É–∞—Ç—É—Ä—É —Å –∫–Ω–æ–ø–∫–∞–º–∏ "–¥–∞–≤–∞–π" –∏ "–Ω–µ —Ö–æ—á—É"
"""
–£ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã yes_no_kb –¥–æ–±–∞–≤–ª—è–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä one_time_keyboard=True, –ø–æ—Ç–æ–º—É —á—Ç–æ —ç—Ç–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ 
–¥–æ–ª–∂–Ω–∞ —Å–≤–æ—Ä–∞—á–∏–≤–∞—Ç—å—Å—è –ø–æ—Å–ª–µ –Ω–∞–∂–∞—Ç–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞ –∫–Ω–æ–ø–∫—É —Å —Ç–µ–∫—Å—Ç–æ–º "–ù–µ —Ö–æ—á—É!". 
–ê –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–º–µ—Ç –Ω–∞ –∫–Ω–æ–ø–∫—É "–î–∞–≤–∞–π!" - –∫–ª–∞–≤–∏–∞—Ç—É—Ä–∞ yes_no_kb –±—É–¥–µ—Ç –∑–∞–º–µ–Ω–µ–Ω–∞ –Ω–∞ –∏–≥—Ä–æ–≤—É—é –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É. 
"""
yes_no_kb: ReplyKeyboardMarkup=yes_no_kb_builder.as_markup(
    one_time_keyboard=True,
    resize_keyboard=True
)

# ------- –°–æ–∑–¥–∞–µ–º –∏–≥—Ä–æ–≤—É—é –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –±–µ–∑ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –±–∏–ª–¥–µ—Ä–∞ -------
# –°–æ–∑–¥–∞–µ–º –∫–Ω–æ–ø–∫–∏ –∏–≥—Ä–æ–≤–æ–π –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã
button_1 = KeyboardButton(text=LEXICON_RU['rock'])
button_2 = KeyboardButton(text=LEXICON_RU['scissors'])
button_3 = KeyboardButton(text=LEXICON_RU['paper'])

# –°–æ–∑–¥–∞–µ–º –∏–≥—Ä–æ–≤—É—é –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –∫–Ω–æ–ø–∫–∞–º–∏ "–ö–∞–º–µ–Ω—å üóø",
# "–ù–æ–∂–Ω–∏—Ü—ã ‚úÇ" –∏ "–ë—É–º–∞–≥–∞ üìú" –∫–∞–∫ —Å–ø–∏—Å–æ–∫ —Å–ø–∏—Å–∫–æ–≤
game_kb = ReplyKeyboardMarkup(
    keyboard=[[button_1],
              [button_2],
              [button_3]],
    resize_keyboard=True
)